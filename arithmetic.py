def add(num1, num2):
    # print "Adding %r and %r" % (num1, num2)
    return num1 + num2

def subtract(num1, num2):
    # print "Subtract %r and %r" % (num1, num2)
    return num1 - num2

def multiply(num1, num2):
    # print "Multiplying %r and %r." % (num1, num2)
    return num1 * num2

def divide(num1, num2):
    # print "Dividing %r and %r." % (num1, num2)
    return float(num1) / float(num2)

def square(num1):
    # print "Squaring %r." % (num1)
    return num1 * num1

def cube(num1):
    # print "Cubing %r." % num1
    return num1 * num1 * num1

def power(num1, num2):
    # print "Finding the power of %r to the %r." % (num1, num2)
    return num1 ** num2

def mod(num1, num2):
    # print "Finding the modulus of %r and %r." % (num1, num2)
    return num1 % num2 